package org.firstinspires.ftc.teamcode.haaslibs;

public class Gamepiece {
    public double x;
    public double y;
    public double width;
    public double height;
    public String color;

    Gamepiece(double x, double y, double width, double height, String color) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = color;
    }
}
